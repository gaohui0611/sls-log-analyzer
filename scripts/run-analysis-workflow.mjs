#!/usr/bin/env node
/**
 * Unified Code Analysis Workflow
 *
 * Runs the complete analysis pipeline:
 *   1. Local static analysis (analyze-codebase.mjs)
 *   2. AI-powered code review (ai-code-review.mjs)
 *   3. Consolidated report generation
 *
 * Usage:
 *   node scripts/run-analysis-workflow.mjs [options]
 *
 * Options:
 *   --local-only   Skip AI review, run only static analysis
 *   --ai-only      Skip static analysis, run only AI review
 *   --verbose      Show detailed output
 */

import { execSync } from 'child_process';
import fs from 'fs/promises';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const ROOT = path.join(__dirname, '..');

const colors = {
    reset: '\x1b[0m',
    red: '\x1b[31m',
    green: '\x1b[32m',
    yellow: '\x1b[33m',
    blue: '\x1b[34m',
    cyan: '\x1b[36m',
    dim: '\x1b[2m',
};

function banner() {
    console.log(`${colors.cyan}
╔═══════════════════════════════════════════════════════════════╗
║                                                               ║
║     🚀  Unified Code Analysis Workflow                          ║
║                                                               ║
║     Phase 1: Local Static Analysis                            ║
║     Phase 2: AI-Powered Code Review                           ║
║                                                               ║
╚═══════════════════════════════════════════════════════════════╝${colors.reset}\n`);
}

function runScript(scriptPath, label) {
    const fullPath = path.join(ROOT, scriptPath);
    console.log(`${colors.cyan}\n▶ ${label}${colors.reset}\n`);
    try {
        const output = execSync(`node "${fullPath}"`, {
            cwd: ROOT,
            encoding: 'utf-8',
            stdio: 'inherit'
        });
        return { success: true, output };
    } catch (error) {
        return { success: false, error: error.message };
    }
}

async function generateConsolidatedReport() {
    const reportsDir = path.join(ROOT, 'reports');
    const files = await fs.readdir(reportsDir).catch(() => []);

    // Find latest reports
    const analysisReports = files
        .filter(f => f.startsWith('codebase-analysis-') && f.endsWith('.md'))
        .sort().reverse();

    const aiReports = files
        .filter(f => f.startsWith('ai-code-review-') && f.endsWith('.md'))
        .sort().reverse();

    const latestAnalysis = analysisReports[0];
    const latestAI = aiReports[0];

    if (!latestAnalysis && !latestAI) {
        console.log(`${colors.yellow}No reports found to consolidate.${colors.reset}`);
        return;
    }

    let content = `# Unified Code Analysis Report

Generated: ${new Date().toISOString()}

## Quick Links

`;

    if (latestAnalysis) {
        content += `- [Static Analysis Report](${latestAnalysis})\n`;
    }
    if (latestAI) {
        content += `- [AI Code Review](${latestAI})\n`;
    }

    content += `\n---

*Generated by run-analysis-workflow.mjs*
`;

    const summaryPath = path.join(reportsDir, 'LATEST_ANALYSIS.md');
    await fs.writeFile(summaryPath, content, 'utf-8');

    console.log(`${colors.green}Summary report: ${summaryPath}${colors.reset}`);
}

// ── Main ────────────────────────────────────────────────────────────────────

async function main() {
    const args = process.argv.slice(2);
    const localOnly = args.includes('--local-only');
    const aiOnly = args.includes('--ai-only');

    if (localOnly && aiOnly) {
        console.error(`${colors.red}Error: Cannot use both --local-only and --ai-only${colors.reset}`);
        process.exit(1);
    }

    banner();

    const phases = [];

    // Phase 1: Static Analysis
    if (!aiOnly) {
        const result = runScript('scripts/analyze-codebase.mjs', 'Phase 1: Local Static Analysis');
        phases.push({ name: 'Static Analysis', ...result });
    }

    // Phase 2: AI Review
    if (!localOnly) {
        const result = runScript('scripts/ai-code-review.mjs', 'Phase 2: AI-Powered Code Review');
        phases.push({ name: 'AI Review', ...result });
    }

    // Summary
    console.log(`\n${colors.cyan}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${colors.reset}`);
    console.log(`${colors.cyan}  Workflow Summary${colors.reset}`);
    console.log(`${colors.cyan}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${colors.reset}\n`);

    for (const phase of phases) {
        const icon = phase.success ? '✅' : '❌';
        const color = phase.success ? 'green' : 'red';
        console.log(`${icon} ${colors[color]}${phase.name}${colors.reset}`);
    }

    // Generate consolidated report
    await generateConsolidatedReport();

    console.log(`\n${colors.cyan}All done! Check the reports/ directory for results.${colors.reset}\n`);
}

main().catch(console.error);
